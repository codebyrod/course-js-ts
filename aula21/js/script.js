/* 
OPERADORES DE COMPARAÇÃO
> maior que
>= maior que ou igual a
< menor que
<= menor que ou igual a
== igualdade (valor) //NÃO RECOMENDÁVEL USAR ESSE
=== igualdade estrita (valor e tipo)
!= diferente (valor) // NÃO RECOMENDÁVEL USAR ESSE
!== diferente estrito (valor e tipo)
*/

//MAIOR QUE OU IGUAL A
console.log(10 > 5 ); //TRUE
console.log(10 >= 5 ); //TRUE
console.log(10 >= 10 ); //TRUE
console.log(10 >= 11 ); //FALSE

//MENOR QUE OU IGUAL A
console.log(10 < 11 ); //TRUE
console.log(10 <= 11 ); //TRUE

// == e != não usamos o comparador de valor simples pq ele faz uma conversão automática de tipos
// usamos === e !== que faz uma comparação de igualdade estrita valor e tipo

